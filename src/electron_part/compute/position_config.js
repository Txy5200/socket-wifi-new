// 定义二维数组
const initArray = (m, n) => {
  let array = []
  for (let i = 0; i < m; i++) {
    let itemArray = []
    for (let j = 0; j < n; j++) {
      itemArray.push(0)
    }
    array.push(itemArray)
  }
  return array
}

